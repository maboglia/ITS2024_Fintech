package model;

public class OrdinazioneTavolo {

	private int id;
	private int tavolo;
	private Ordinazione ordinazione;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getTavolo() {
		return tavolo;
	}
	public void setTavolo(int tavolo) {
		this.tavolo = tavolo;
	}
	public Ordinazione getOrdinazione() {
		return ordinazione;
	}
	public void setOrdinazione(Ordinazione ordinazione) {
		this.ordinazione = ordinazione;
	}
	
	
	
}
